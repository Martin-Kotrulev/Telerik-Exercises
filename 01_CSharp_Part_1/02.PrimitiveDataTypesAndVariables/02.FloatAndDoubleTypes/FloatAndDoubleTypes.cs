using System;

class FloatAndDoubleTypes
{
    static void Main()
    {
        double var1 = 34.567839023d;
        double var2 = 8923.1234857d;
        float var3 = 12.345f;
        float var4 = 3456.091f;
        Console.WriteLine(
            "{0} and {1} is of the type \"double\".\n{2} and {3} is of the type \"float\"",
            var1, var2, var3, var4);
    }
}

